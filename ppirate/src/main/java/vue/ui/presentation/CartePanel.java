    // Vérifie si un clic est à l'intérieur de la carte.
    public boolean clicIn(int pX, int pY) {
        boolean dansLargeur = pX >= posX && pX <= posX + dimX;
        boolean dansHauteur = pY >= posY && pY <= posY + dimY;
        return dansLargeur && dansHauteur;
    }
    

